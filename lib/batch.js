// select 5, 10, or 15 random records from trainingData
const newBatch = (size) => {
  const trainingRecordCount = trainingData.length;
  const records = [];

  for (let i = 0; i < size; i++) {
    const ri = Math.floor(Math.random() * trainingRecordCount); // random index
    records.push(trainingData[ri]); // select random index in range and push
  }

  return records;
};

// generate request body for POST to ingest API
const newRequest = (batch) => {
  const subjects = [];
  const features = [];

  batch.map(({id, variance, skewness, curtosis, entropy}) => {
    subjects.push(id);
    features.push([variance, skewness, curtosis, entropy]);
  });

  return {
    subjects,
    features,
  };
};

const submitBatch = async (url, request) => {
  const response = await fetch(url + '/notes', {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
    },
    body: JSON.stringify(request),
  });
  const res = await response.json();

  return res;
};

const submitCorrectedBatch = async (url, request) => {
  const response = await fetch(url + `/batches/${request.batch_id}`, {
    method: 'PATCH',
    headers: {
      'Content-Type': 'application/json',
    },
    body: JSON.stringify(request),
  });
  const res = await response.json();

  return res;
};

